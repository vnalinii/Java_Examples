package com.heera.java;

public class IncrementDecrementOperatorDemo {

	public static void main(String[] args) {
		int i=10;
		System.out.println(i);
		i++;
		System.out.println(i);
		++i;
		System.out.println(i);
		System.out.println(++i);
		System.out.println(i++);
		System.out.println(i);
	}
}